package java_start.basic;

// class 명은 파일 명과 동일해야 한다.
public class HelloJava {
    // main 메서드
    // 자바는 main(String[] args) 메서드를 찾아 프로그램을 실행한다.
    public static void main(String[] args) { // 메서드 명 및 인자 선언 후 블록으로 범위를 지정한다.
        // 출력하는 메서드
        System.out.println("hello java"); // 자바는 한 문장이 끝날때 마다 세미콜론을 사용한다.
    }
}
