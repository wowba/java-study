package java_start.if_else_switch;

public class If_else {
    public static void main(String[] args) {
        int age = 20;

        if (age >= 18) {
            System.out.println("adult");
        } else if (age >= 90) {
            System.out.println("old");
        } else {
            System.out.println("young");
        }

        if (age >= 18) System.out.println("adult!!!!!");
    }
}
